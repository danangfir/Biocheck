daftar_angka = [1, 2, 3, 4, 5]

# Menggunakan pop tanpa argumen
angka_terhapus = daftar_angka.pop()
print(angka_terhapus)  # Output: 5
print(daftar_angka)  # Output: [1, 2, 3, 4]

# Menggunakan pop dengan memberikan indeks
angka_terhapus = daftar_angka.pop(1)
print(angka_terhapus)  # Output: 2
print(daftar_angka)  # Output: [1, 3, 4]

teks = "Halo  dunia!"
